async function style(text, style = 1) {
  const replacer = xStr.map((v, i) => ({
    original: v,
    convert: yStr[style][i],
  }));

  const str = text.toLowerCase().split('');
  const output = str.map(v => {
    const find = replacer.find(x => x.original === v);
    return find ? find.convert : v;
  });

  return output.join('');
}

